# ── Data model ─────────────────────────────────────────────────────────────────

@dataclass
class Result:
    url:           str
    method:        str
    fmt:           str
    concurrency:   int
    response_time: float
    status_code:   Optional[int]
    success:       bool
    error:         Optional[str] = None
    error_type:    Optional[str] = None  # timeout | connection | http_NNN | json_parse | empty_body | other
    html_snippet:  str = ""  # fetched HTML (from 424 detail.content) for pattern analysis
    text_snippet:  str = ""  # first chars of extracted text (success only)
    text_length:   int = 0

# ── Error classification ──────────────────────────────────────────────────────

def classify_error(exc: Exception) -> str:
    """Map an exception to a short category label."""
    name = type(exc).__name__
    if isinstance(exc, asyncio.TimeoutError) or "Timeout" in name:
        return "timeout"
    if isinstance(exc, (
        aiohttp.ClientConnectorError,
        aiohttp.ServerDisconnectedError,
        aiohttp.ClientConnectionError,
    )) or "Connect" in name or "Disconnect" in name:
        return "connection"
    if isinstance(exc, json.JSONDecodeError) or "JSON" in name or "Decode" in name:
        return "json_parse"
    return f"other:{name}"

# ── Core async logic ───────────────────────────────────────────────────────────

async def fetch(
    session: aiohttp.ClientSession,
    sem: asyncio.Semaphore,
    url: str,
    method: str,
    fmt: str,
    concurrency: int,
) -> Result:
    payload = {
        "url": url,
        "method": method,
        "output_format": fmt,
        "lang": "auto",
        "preference": "none",
    }
    t0 = time.monotonic()
    async with sem:
        try:
            async with session.post(
                API_ENDPOINT,
                json=payload,
                timeout=aiohttp.ClientTimeout(total=REQUEST_TIMEOUT),
            ) as resp:
                elapsed = time.monotonic() - t0
                raw = await resp.text()
                if resp.status != 200:
                    # Try to extract HTML snippet from service error JSON (e.g. 424)
                    html_snip = ""
                    try:
                        err_body = json.loads(raw)
                        detail = err_body.get("detail", {})
                        if isinstance(detail, dict):
                            html_snip = (detail.get("content") or "")[:4000]
                            err_msg   = detail.get("error_message") or detail.get("reason") or ""
                        else:
                            err_msg = str(detail)[:1000]
                    except Exception:
                        err_msg = raw[:1000]
                    return Result(
                        url, method, fmt, concurrency,
                        elapsed, resp.status, False,
                        error=err_msg or raw[:1000],
                        error_type=f"http_{resp.status}",
                        html_snippet=html_snip,
                    )
                try:
                    body = json.loads(raw)
                except json.JSONDecodeError as jexc:
                    return Result(
                        url, method, fmt, concurrency,
                        elapsed, resp.status, False,
                        error=str(jexc),
                        error_type="json_parse",
                    )
                text = body.get("text", "") if isinstance(body, dict) else ""
                if not text:
                    return Result(
                        url, method, fmt, concurrency,
                        elapsed, resp.status, False,
                        error="empty text in response",
                        error_type="empty_body",
                    )
                return Result(
                    url, method, fmt, concurrency,
                    elapsed, resp.status, True,
                    text_length=len(text),
                    text_snippet=text[:300],
                )
        except Exception as exc:
            return Result(
                url, method, fmt, concurrency,
                time.monotonic() - t0, None, False,
                error=str(exc),
                error_type=classify_error(exc),
            )


def load_test_urls() -> List[str]:
    """Load URLs from URL_FILE if present, otherwise fall back to built-in TEST_URLS."""
    try:
        with open(URL_FILE, encoding="utf-8") as f:
            urls = [line.strip() for line in f if line.strip()]
        if urls:
            print(f"Loaded {len(urls)} URLs from {URL_FILE}")
            return urls
    except FileNotFoundError:
        pass
    print(f"({URL_FILE} not found – using built-in TEST_URLS, run sample_urls.py first)")
    return TEST_URLS


async def run_level(method: str, fmt: str, concurrency: int,
                    urls: List[str]) -> List[Result]:
    sem = asyncio.Semaphore(concurrency)
    async with aiohttp.ClientSession() as session:
        tasks = [
            fetch(session, sem, url, method, fmt, concurrency)
            for url in urls
        ]
        return await asyncio.gather(*tasks)


async def run_all() -> List[Result]:
    urls  = load_test_urls()
    total = len(METHODS) * len(FORMATS) * len(CONCURRENCY_LEVELS)
    step  = 0
    all_results: List[Result] = []

    print(
        f"Total requests: {len(urls) * total}  "
        f"({len(urls)} URLs x {len(METHODS)} methods x "
        f"{len(FORMATS)} formats x {len(CONCURRENCY_LEVELS)} concurrency levels)\n"
    )

    for method in METHODS:
        for fmt in FORMATS:
            for conc in CONCURRENCY_LEVELS:
                step += 1
                label = (
                    f"[{step:>2}/{total}] "
                    f"method={method:<7} format={fmt:<8} conc={conc:>2}"
                )
                print(f"{label} ...", end=" ", flush=True)
                t0 = time.monotonic()
                results = await run_level(method, fmt, conc, urls)
                wall = time.monotonic() - t0
                ok = [r for r in results if r.success]
                avg = statistics.mean(r.response_time for r in ok) if ok else 0.0
                print(
                    f"ok={len(ok)}/{len(results)}  "
                    f"avg={avg:.1f}s  wall={wall:.1f}s"
                )
                all_results.extend(results)

    return all_results

# ── Plotting ───────────────────────────────────────────────────────────────────

def plot(results: List[Result], save_path: str) -> None:
    n_urls = len(set(r.url for r in results))
    combinations = [(m, f) for m in METHODS for f in FORMATS]
    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    fig.suptitle(
        "Load Test - Text Extraction Service\n"
        f"({n_urls} URLs  x  {len(CONCURRENCY_LEVELS)} concurrency levels)",
        fontsize=13,
        fontweight="bold",
    )

    for idx, (method, fmt) in enumerate(combinations):
        ax = axes[idx // 2][idx % 2]
        subset = [r for r in results if r.method == method and r.fmt == fmt]

        avgs, p50s, p95s, err_pcts = [], [], [], []
        for conc in CONCURRENCY_LEVELS:
            grp      = [r for r in subset if r.concurrency == conc]
            ok_times = [r.response_time for r in grp if r.success]
            avgs.append(statistics.mean(ok_times)           if ok_times else float("nan"))
            p50s.append(float(np.percentile(ok_times, 50))  if ok_times else float("nan"))
            p95s.append(float(np.percentile(ok_times, 95))  if ok_times else float("nan"))
            err_pcts.append(
                (len(grp) - len(ok_times)) / len(grp) * 100 if grp else 0
            )

        ax.plot(CONCURRENCY_LEVELS, avgs, "o-",  color="steelblue",  label="Mean",  lw=2)
        ax.plot(CONCURRENCY_LEVELS, p50s, "s--", color="seagreen",   label="P50",   lw=1.5)
        ax.plot(CONCURRENCY_LEVELS, p95s, "^:",  color="darkorange", label="P95",   lw=1.5)
        ax.set_title(f"method={method}  /  format={fmt}", fontsize=11)
        ax.set_xlabel("Concurrency")
        ax.set_ylabel("Response time (s)")
        ax.legend(loc="upper left", fontsize=8)
        ax.set_xticks(CONCURRENCY_LEVELS)
        ax.grid(True, alpha=0.3)

        ax2 = ax.twinx()
        ax2.bar(
            CONCURRENCY_LEVELS, err_pcts,
            width=0.5, alpha=0.25, color="tomato", label="Error %",
        )
        ax2.set_ylabel("Error rate (%)", color="tomato", fontsize=8)
        ax2.set_ylim(0, 105)
        ax2.tick_params(axis="y", labelcolor="tomato")

    plt.tight_layout()
    plt.savefig(save_path, dpi=150, bbox_inches="tight")
    print(f"\nPlot saved  -> {save_path}")
    plt.show()

